public class Detergent {
	//清洁用品类Detergent
	private String name;//清洁用品名称
	private int energy;//清洁用品的能量值
	
	public String getName() {
		return name;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public int getEnergy() {
		return energy;
	}
	
	public void setEnergy(int energy) {
		this.energy = energy;
	}

}
