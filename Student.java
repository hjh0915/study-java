class Student {
    String name;
    int age;
    String sex;

    public Student (String n, int a, String s) {
        name = n;
        age = a;
        sex = s;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }

    public String getSex() {
        return sex;
    }
}